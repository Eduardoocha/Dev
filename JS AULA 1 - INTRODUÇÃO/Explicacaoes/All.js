
function calcularAreaRetangulo() 

{
 let base = 5; // Criamos uma caixinha chamada "base" e guardamos o valor 5 nela
 let altura = 3; // Criamos outra caixinha chamada "altura" e guardamos o valor 3 nela
 let area = base * altura; // Criamos uma nova caixinha chamada "area" e calculamos o resultado
 console.log("A área do retângulo é:", area); // Exibimos o resultado no console
}


calcularAreaRetangulo();

var nota1 = 7; // Criamos uma caixinha chamada "nota1" e guardamos o valor 7 nela
 var nota2 = 8; // Criamos outra caixinha chamada "nota2" e guardamos o valor 8 nela
 var nota3 = 6; // Criamos uma terceira caixinha chamada "nota3" e guardamos o valor 6 nela

 function calcularMedia() 

 {
 var media = (nota1 + nota2 + nota3) / 3; // Criamos uma nova caixinha chamada "media" e calculamos a média
 console.log("A média das notas é:", media); // Exibimos o resultado no console
}

calcularMedia(); // Chamamos a função para calcular e exibir a média das notas
