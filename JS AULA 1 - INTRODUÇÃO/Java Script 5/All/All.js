//Expressão com parenteses:

let resultado1 = (2 + 3) * 4; 

console.log(resultado1); // Resultado: 20 



//Combinação de soma e subtração:

let resultado2 = 10 + 5 - 2;

console.log(resultado2); // Resultado: 13 



//Combinação de soma, subtração e multiplicação:

let resultado3 = (10 + 5) * 2 - 3;

console.log(resultado3); // Resultado: 27



//Utilizando variáveis em uma expressão:

let num1 = 3;

let num2 = 4;

let resultado4 = num1 * (num1 + num2);

console.log(resultado4); // Resultado: 21



//Utilizando constantes em uma expressão:

const PI = 3.14;

let raio = 5;

let area = PI * raio * raio.

console.log(area); // Resultado: 78.5